class While20
{
   public static void main( String[] not_in_use )
   {
      double  number_to_print  =  0 ;

      System.out.print( "\n Numbers from 0 to 20 are the following:\n\n ") ;

      while ( number_to_print  <= 20 )
      {
         System.out.print( " "  +  number_to_print ) ;
         number_to_print  ++  ;
      }
   }
}



